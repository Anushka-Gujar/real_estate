    return false;
}
if (FirstCharStr2.test(name)) {
    seterror("name", "*Please enter a valid name");
    alert("You must enter valid username");
    return false;
}
if (FirstCharStr3.test(name)) {
    seterror("name", "*Please enter a valid name");
